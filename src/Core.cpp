std::vector<Vertex> MakeVertexFromFloat(std::vector<float> f_vertices) 
{
    std::vector<Vertex> v_vertices;
    for(int i = 0; i < f_vertices.size() / 8; i++) {
        Vertex vertex;
        vertex.Position = {f_vertices[(i*8) + 0], f_vertices[(i*8) + 1], f_vertices[(i*8) + 2]};
        vertex.Normal = {f_vertices[(i*8) + 3], f_vertices[(i*8) + 4], f_vertices[(i*8) + 5]};
        vertex.UV = {f_vertices[(i*8) + 6], f_vertices[(i*8) + 7]};
        v_vertices.push_back(vertex);
    }
    return v_vertices;
}